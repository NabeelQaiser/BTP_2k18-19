CREATE OR REPLACE PROCEDURE TEST(X IN VARCHAR, Y IN VARCHAR)
IS
BEGIN
  ASSUME A + X > 12;
  A INT;
  B VARCHAR;
  CURSOR GET_TYPE_ID IS
        SELECT A
        FROM T
        WHERE B = 50;
  CURSOR GET_MAX_TYPE_ID IS
        SELECT B
        FROM T;

  BEGIN
      OPEN HELLO;

      FETCH HELLO INTO B;

      CLOSE HELLO;

      UPDATE T SET A=A*(X-9*(Y-3)), B=Y-9 WHERE NOT A>10 AND B<=(X+Y)-50;
      ASSERT A>0;
  END;
END;
