CREATE PROCEDURE GET_EMP_REC (EMP_NUMBER  IN  EMP_TAB.EMPNO%TYPE,
                              EMP_RET     OUT EMP_TAB%ROWTYPE) AS
BEGIN
	ASSUME EMP_RET > 0 ;

   SELECT EMPNO, ENAME, JOB, MGR, HIREDATE, SAL, COMM, DEPTNO
      INTO EMP_RET
      FROM EMP_TAB
      WHERE EMPNO = EMP_NUMBER;
ASSERT EMP_RET > 0 ;
	END;